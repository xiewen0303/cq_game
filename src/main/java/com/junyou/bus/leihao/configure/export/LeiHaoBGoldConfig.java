package com.junyou.bus.leihao.configure.export;

/**
 * 累计消耗领取绑元配置
 * @author zhongdian
 *
 */
public class LeiHaoBGoldConfig {
	private int id;
	private int xfvalue;
	private int bgold ;
	private int fhb;
	
	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getXfvalue() {
		return xfvalue;
	}

	public void setXfvalue(int xfvalue) {
		this.xfvalue = xfvalue;
	}

	public int getBgold() {
		return bgold;
	}

	public void setBgold(int bgold) {
		this.bgold = bgold;
	}

	public int getFhb() {
		return fhb;
	}

	public void setFhb(int fhb) {
		this.fhb = fhb;
	}



}
