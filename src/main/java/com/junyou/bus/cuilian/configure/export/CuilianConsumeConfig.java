package com.junyou.bus.cuilian.configure.export;

public class CuilianConsumeConfig {
	private int times;
	private long needMoney;
	private int needBgold;
	private int needGold;

	public int getTimes() {
		return times;
	}

	public void setTimes(int times) {
		this.times = times;
	}

	public long getNeedMoney() {
		return needMoney;
	}

	public void setNeedMoney(long needMoney) {
		this.needMoney = needMoney;
	}

	public int getNeedBgold() {
		return needBgold;
	}

	public void setNeedBgold(int needBgold) {
		this.needBgold = needBgold;
	}

	public int getNeedGold() {
		return needGold;
	}

	public void setNeedGold(int needGold) {
		this.needGold = needGold;
	}

}
